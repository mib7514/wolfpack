export function parseAIResponse(text) {
  try {
    const clean = text.replace(/```json|```/g, "").trim();
    return JSON.parse(clean);
  } catch {
    return null;
  }
}

export function formatDateLabel(d) {
  const [y, m] = d.split("-");
  return `${y.slice(2)}.${m}`;
}

// 밸류업지수 vs KOSPI 인덱싱 (밸류업지수 있는 시점부터)
export function indexValueup(data) {
  const start = data.find((d) => d.valueup_index != null);
  if (!start) return data;
  return data.map((d) => ({
    ...d,
    kospiIdx: +((d.kospi_close / start.kospi_close) * 100).toFixed(1),
    valueupIdx: d.valueup_index != null ? +((d.valueup_index / start.valueup_index) * 100).toFixed(1) : null,
  }));
}
